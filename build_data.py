# --- 3. Configuration électronique + couches (aufbau) ---
ORDER = [(1,0),(2,0),(2,1),(3,0),(3,1),(4,0),(3,2),(4,1),(5,0),(4,2),(5,1),
         (6,0),(4,3),(5,2),(6,1),(7,0),(5,3),(6,2),(7,1)]
CAP = {0:2,1:6,2:10,3:14}
LBL = {0:'s',1:'p',2:'d',3:'f'}

def electrons(z):
    left = z
    sub = {}
    for (n,l) in ORDER:
        if left <= 0: break
        c = min(CAP[l], left)
        sub[(n,l)] = c
        left -= c
    return sub

def config(z):
    sub = electrons(z)
    parts = []
    for (n,l) in sorted(sub, key=lambda k:(k[0],k[1])):
        parts.append("%d%s%d" % (n, LBL[l], sub[(n,l)]))
    return " ".join(parts)

def shells(z):
    sub = electrons(z)
    sh = {}
    for (n,l),c in sub.items():
        sh[n] = sh.get(n,0) + c
    return [sh[n] for n in sorted(sh)]

